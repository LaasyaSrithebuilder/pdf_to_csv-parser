import pdfplumber
from interfaces import TextExtractor

class PdfPlumberExtractor(TextExtractor):
    def extract(self, source: str) -> str:
        text = ""
        with pdfplumber.open(source) as pdf:
            for page in pdf.pages:
                page_text = page.extract_text()
                if page_text:
                    text += page_text + "\n"
        return text
